import os

from langchain_openai import ChatOpenAI
from typing import List
from pydantic import BaseModel, Field
from dotenv import load_dotenv

load_dotenv()

# --- Конфигурация OpenAI ---
PROXYAPI_KEY = os.getenv("PROXYAPI_KEY")
if not PROXYAPI_KEY:
    raise ValueError("PROXYAPI_KEY не найден в переменных окружения")

BASE_URL = os.getenv("OPENAI_BASE_URL")
if not BASE_URL:
    raise ValueError("BASE_URL не найден в переменных окружения")

llm = ChatOpenAI(
    model_name="gpt-4o-mini",
    temperature=0.2,
    openai_api_key=PROXYAPI_KEY,
    openai_api_base=BASE_URL
)


# Задаем последовательность шагов рассуждения
class ProductCard(BaseModel):
    """Структура карточки товара для маркетплейса"""

    # --- Блок задачи ---
    task: str = Field(
        description=(
            "Постановка задачи: что именно нужно сгенерировать и для какой цели "
            "(например: создать карточку товара для маркетплейса Ozon/Wildberries "
            "на основе пользовательского запроса и контекста)."
        )
    )

    context = """
    Беспроводные наушники с активным шумоподавлением и встроенным микрофоном созданы специально для тренировок и активного образа жизни.
    Они обеспечивают чистое, мощное звучание даже в шумной обстановке благодаря технологии ANC.

    Эргономичная форма и мягкие амбушюры гарантируют комфортную посадку и устойчивость во время движений.
    Влагозащита по стандарту IPX5 защищает устройство от пота и дождя, что делает их идеальными для занятий спортом на улице или в зале.
    Встроенный микрофон с системой шумоподавления позволяет принимать звонки и использовать голосовые команды без прерывания тренировки.
    Поддержка Bluetooth 5.3 обеспечивает стабильное соединение без задержек. Наушники работают до 8 часов от одного заряда, а с зарядным кейсом — до 24 часов.
    Поддерживается быстрая зарядка: 10 минут = 1,5 часа воспроизведения. Совместимы с Android и iOS.

    Характеристики: беспроводное подключение Bluetooth 5.3; активное шумоподавление (ANC); встроенный микрофон; влагозащита IPX5; время автономной работы — до 8 часов (до 24 часов с кейсом);
    тип конструкции — внутриканальные; диапазон частот — 20 Гц–20 кГц; ёмкость аккумулятора — 40 мА·ч в наушнике, 400 мА·ч в кейсе; время зарядки — около 1,5 часа;
    интерфейс зарядки — USB Type-C; вес одного наушника — 4,5 г; цвет — чёрный.
    """
    # --- Контекст ---
    context: str = Field(
        description=f"Контекст по товару - {context}"
    )

    # --- Блок рассуждения (Chain-of-Reasoning) ---
    reasoning: str = Field(
        description=(
            "Пошаговые рассуждения модели: определить категорию товара, целевую аудиторию, "
            "ключевые выгоды (УТП), важные характеристики, возможные недостающие данные "
            "и подходящий стиль описания. "
            "Модель должна рассуждать, как маркетолог, объясняя выбор каждой детали."
        )
    )

    # --- Блок проверки (Chain-of-Verification) ---
    verification: str = Field(
        description=(
            "Проверка логики и данных: убедись, что описание согласовано с контекстом, "
            "выгоды соответствуют категории, отсутствуют повторы и противоречия. "
            "При необходимости кратко предложи корректировки."
        )
    )

    # --- Блок итогового вывода (результат) ---
    title: str = Field(description="Название товара с уникальным торговым предложением (УТП).")
    short_description: str = Field(description="Краткое описание (1–2 предложения, фокус на выгоде).")
    full_description: str = Field(description="Полное описание (5–7 предложений, раскрывающее сценарии использования).")
    advantages: List[str] = Field(description="4–5 ключевых преимуществ товара (в формате списка).")
    characteristics: List[str] = Field(description="Характеристики товара (параметр: значение).")
    seo_keywords: List[str] = Field(description="SEO-ключевые слова через запятую.")


# Оборачиваем LLM так, что бы она следовала структуре выше
structured_llm = llm.with_structured_output(ProductCard)

query = 'Наушники с шумоподавлением и микрофоном для тренировок'
result = structured_llm.invoke(query)
print(result.model_dump_json(indent=2))
